import React, { useState } from 'react';
import { X, Mail, Loader2, AlertCircle } from 'lucide-react';
import { api } from '../../services/apiClient';

interface AuthModalProps {
  onClose?: () => void;
  onLogin: (user: any) => void;
  isForced?: boolean;
}

export const AuthModal: React.FC<AuthModalProps> = ({ onClose, onLogin, isForced = false }) => {
  const [isLogin, setIsLogin] = useState(true);
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [username, setUsername] = useState('');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');

  const handleAuth = async (e: React.FormEvent) => {
    e.preventDefault();
    setError('');
    setLoading(true);

    try {
      if (isLogin) {
        const data = await api.post('/auth/login', { email, password });
        api.setToken(data.token);
        onLogin(data.user);
      } else {
        const data = await api.post('/auth/signup', { email, password, username });
        api.setToken(data.token);
        onLogin(data.user);
      }
    } catch (err: any) {
      setError(err.message || 'Authentication failed');
    } finally {
      setLoading(false);
    }
  };

  const handleSocialLogin = async (platform: string) => {
    // Redirect to backend OAuth endpoint
    const apiBaseUrl = import.meta.env.VITE_API_URL || '/api';
    // Remove trailing slash if present to avoid double slashes
    const baseUrl = apiBaseUrl.endsWith('/') ? apiBaseUrl.slice(0, -1) : apiBaseUrl;

    const oauthUrl = platform === 'Google'
      ? `${baseUrl}/auth/google`
      : `${baseUrl}/auth/discord`;

    window.location.href = oauthUrl;
  };

  return (
    <div className="fixed inset-0 z-[60] flex items-center justify-center bg-black/80 backdrop-blur-sm p-4 animate-fade-in">
      <div className="bg-[#111] w-full max-w-md rounded-3xl border border-white/10 shadow-2xl overflow-hidden relative animate-scale-in">
        {!isForced && (
          <button onClick={onClose} className="absolute top-4 right-4 text-[#666] hover:text-white transition-colors">
            <X size={24} />
          </button>
        )}
        <div className="p-8">
          <div className="text-center mb-8">
            <h2 className="text-2xl font-bold text-white mb-2">{isLogin ? 'Welcome Back' : 'Create Account'}</h2>
            <p className="text-[#666] text-sm font-bold uppercase tracking-widest">AnyReps Authentication</p>
          </div>

          <form onSubmit={handleAuth} className="space-y-4">
            {!isLogin && (
              <div className="animate-fade-in-up">
                <label className="block text-[10px] font-bold text-[#666] uppercase mb-1.5 ml-1">Username</label>
                <input type="text" value={username} onChange={e => setUsername(e.target.value)} required className="w-full bg-[#0A0A0A] border border-white/5 rounded-xl p-3.5 text-white outline-none focus:border-primary transition-all" placeholder="reps_king" />
              </div>
            )}
            <div className="animate-fade-in-up" style={{ animationDelay: '0.1s' }}>
              <label className="block text-[10px] font-bold text-[#666] uppercase mb-1.5 ml-1">Email Address</label>
              <input type="email" value={email} onChange={e => setEmail(e.target.value)} required className="w-full bg-[#0A0A0A] border border-white/5 rounded-xl p-3.5 text-white outline-none focus:border-primary transition-all" placeholder="name@example.com" />
            </div>
            <div className="animate-fade-in-up" style={{ animationDelay: '0.2s' }}>
              <label className="block text-[10px] font-bold text-[#666] uppercase mb-1.5 ml-1">Password</label>
              <input type="password" value={password} onChange={e => setPassword(e.target.value)} required className="w-full bg-[#0A0A0A] border border-white/5 rounded-xl p-3.5 text-white outline-none focus:border-primary transition-all" placeholder="••••••••" />
            </div>

            {error && (
              <div className="p-3 bg-red-500/10 border border-red-500/20 text-red-400 text-xs rounded-lg flex items-center gap-2 animate-shake">
                <AlertCircle size={14} className="shrink-0" /> <span>{error}</span>
              </div>
            )}

            <button type="submit" disabled={loading} className="w-full py-4 bg-primary hover:bg-primaryHover text-black font-bold rounded-xl flex items-center justify-center gap-2 transition-all shadow-xl shadow-primary/10 hover:shadow-primary/20 mt-2">
              {loading ? <Loader2 className="animate-spin" size={20} /> : (isLogin ? 'Sign In' : 'Create Account')}
            </button>
          </form>

          <div className="relative my-8">
            <div className="absolute inset-0 flex items-center"><div className="w-full border-t border-white/5"></div></div>
            <div className="relative flex justify-center text-[10px] font-bold uppercase tracking-[0.2em]"><span className="bg-[#111] px-4 text-[#444]">Or continue with</span></div>
          </div>

          <div className="grid grid-cols-2 gap-4">
            {/* Google Button */}
            <button
              onClick={() => handleSocialLogin('Google')}
              disabled={loading}
              className="group relative flex items-center justify-center gap-3 py-4 px-4 bg-gradient-to-br from-white/[0.08] to-white/[0.02] border border-white/10 rounded-2xl hover:border-white/20 transition-all duration-300 hover:scale-[1.02] hover:shadow-lg hover:shadow-white/5 disabled:opacity-50 disabled:cursor-not-allowed overflow-hidden"
            >
              {/* Animated background gradient on hover */}
              <div className="absolute inset-0 bg-gradient-to-r from-blue-500/0 via-blue-500/5 to-blue-500/0 opacity-0 group-hover:opacity-100 transition-opacity duration-500" />

              {/* Google Icon */}
              <div className="relative z-10 w-5 h-5 flex items-center justify-center">
                <svg viewBox="0 0 24 24" className="w-5 h-5">
                  <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                  <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                  <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                  <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                </svg>
              </div>

              {/* Text */}
              <span className="relative z-10 text-sm font-bold text-white/90 group-hover:text-white transition-colors">
                Google
              </span>

              {/* Shine effect */}
              <div className="absolute inset-0 -translate-x-full group-hover:translate-x-full transition-transform duration-1000 bg-gradient-to-r from-transparent via-white/10 to-transparent" />
            </button>

            {/* Discord Button */}
            <button
              onClick={() => handleSocialLogin('Discord')}
              disabled={loading}
              className="group relative flex items-center justify-center gap-3 py-4 px-4 bg-gradient-to-br from-[#5865F2]/20 to-[#5865F2]/5 border border-[#5865F2]/30 rounded-2xl hover:border-[#5865F2]/50 hover:bg-gradient-to-br hover:from-[#5865F2]/30 hover:to-[#5865F2]/10 transition-all duration-300 hover:scale-[1.02] hover:shadow-lg hover:shadow-[#5865F2]/20 disabled:opacity-50 disabled:cursor-not-allowed overflow-hidden"
            >
              {/* Animated background */}
              <div className="absolute inset-0 bg-gradient-to-r from-[#5865F2]/0 via-[#5865F2]/10 to-[#5865F2]/0 opacity-0 group-hover:opacity-100 transition-opacity duration-500" />

              {/* Discord Icon */}
              <div className="relative z-10 w-5 h-5 flex items-center justify-center">
                <svg viewBox="0 0 24 24" fill="#5865F2" className="w-5 h-5 group-hover:scale-110 transition-transform duration-300">
                  <path d="M20.317 4.37a19.791 19.791 0 0 0-4.885-1.515a.074.074 0 0 0-.079.037c-.21.375-.444.864-.608 1.25a18.27 18.27 0 0 0-5.487 0a12.64 12.64 0 0 0-.617-1.25a.077.077 0 0 0-.079-.037A19.736 19.736 0 0 0 3.677 4.37a.07.07 0 0 0-.032.027C.533 9.046-.32 13.58.099 18.057a.082.082 0 0 0 .031.057a19.9 19.9 0 0 0 5.993 3.03a.078.078 0 0 0 .084-.028a14.09 14.09 0 0 0 1.226-1.994a.076.076 0 0 0-.041-.106a13.107 13.107 0 0 1-1.872-.892a.077.077 0 0 1-.008-.128a10.2 10.2 0 0 0 .372-.292a.074.074 0 0 1 .077-.01c3.928 1.793 8.18 1.793 12.062 0a.074.074 0 0 1 .078.01c.12.098.246.198.373.292a.077.077 0 0 1-.006.127a12.299 12.299 0 0 1-1.873.892a.077.077 0 0 0-.041.107c.36.698.772 1.362 1.225 1.993a.076.076 0 0 0 .084.028a19.839 19.839 0 0 0 6.002-3.03a.077.077 0 0 0 .032-.054c.5-5.177-.838-9.674-3.549-13.66a.061.061 0 0 0-.031-.03zM8.02 15.33c-1.183 0-2.157-1.085-2.157-2.419c0-1.333.956-2.419 2.157-2.419c1.21 0 2.176 1.096 2.157 2.42c0 1.333-.956 2.418-2.157 2.418zm7.975 0c-1.183 0-2.157-1.085-2.157-2.419c0-1.333.955-2.419 2.157-2.419c1.21 0 2.176 1.096 2.157 2.42c0 1.333-.946 2.418-2.157 2.418z" />
                </svg>
              </div>

              {/* Text */}
              <span className="relative z-10 text-sm font-bold text-white/90 group-hover:text-white transition-colors">
                Discord
              </span>

              {/* Shine effect */}
              <div className="absolute inset-0 -translate-x-full group-hover:translate-x-full transition-transform duration-1000 bg-gradient-to-r from-transparent via-white/10 to-transparent" />
            </button>
          </div>

          <div className="mt-8 text-center">
            <button onClick={() => setIsLogin(!isLogin)} className="text-sm font-bold text-[#666] hover:text-white transition-colors uppercase tracking-widest text-[10px]">
              {isLogin ? "New to AnyReps? Create Account" : "Already have an account? Login"}
            </button>
          </div>
        </div>
      </div>
    </div>
  );
};
